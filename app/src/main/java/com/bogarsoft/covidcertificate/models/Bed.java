package com.bogarsoft.covidcertificate.models;

public class Bed {
    String name;
    String ruralhos;
    String ruralbeds;
    String urbanhos;
    String urbanbeds;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRuralhos() {
        return ruralhos;
    }

    public void setRuralhos(String ruralhos) {
        this.ruralhos = ruralhos;
    }

    public String getRuralbeds() {
        return ruralbeds;
    }

    public void setRuralbeds(String ruralbeds) {
        this.ruralbeds = ruralbeds;
    }

    public String getUrbanhos() {
        return urbanhos;
    }

    public void setUrbanhos(String urbanhos) {
        this.urbanhos = urbanhos;
    }

    public String getUrbanbeds() {
        return urbanbeds;
    }

    public void setUrbanbeds(String urbanbeds) {
        this.urbanbeds = urbanbeds;
    }
}
